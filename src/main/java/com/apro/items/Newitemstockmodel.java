package com.apro.items;

public class Newitemstockmodel {
String id, iname;

public Newitemstockmodel(String id, String iname) {

	this.id = id;
	this.iname = iname;
}

public String getId() {
	return id;
}

public void setId(String id) {
	this.id = id;
}

public String getIname() {
	return iname;
}

public void setIname(String iname) {
	this.iname = iname;
}
}
